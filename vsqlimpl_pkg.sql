	function lum(
		p_color integer
	)
	return number
	deterministic
	as
		v_r integer;
		v_g integer;
		v_b integer;
		v_maxc integer;
		v_minc integer;
	begin
		v_r := attr_color_r(p_color);
		v_g := attr_color_g(p_color);
		v_b := attr_color_b(p_color);

		v_minc := least(v_r, v_g, v_b);
		v_maxc := greatest(v_r, v_g, v_b);
		return (v_minc + v_maxc)/255/2;
	end;

