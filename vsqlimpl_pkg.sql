	function join_str_clob(
		p_separator in varchar2,
		p_list in clob
	)
	return clob
	deterministic
	as
		v_result clob;
	begin
		if p_separator is null then
			return p_list;
		elsif p_list is null then
			return null;
		else
			dbms_lob.createtemporary(v_result, true);
			for i in 1..length(p_list) loop
				if i != 1 then
