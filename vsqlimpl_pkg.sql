	function rgb(
		p_r in number,
		p_g in number,
		p_b in number,
		p_a in number := 1.0
	)
	return integer
	deterministic
	as
		v_r integer;
		v_g integer;
		v_b integer;
		v_a integer;
	begin
		v_r := floor(p_r * 255);
		v_g := floor(p_g * 255);
		v_b := floor(p_b * 255);
		v_a := floor(p_a * 255);

		if v_r < 0 then
			v_r := 0;
		elsif v_r > 255 then
			v_r := 255;
		end if;

		if v_g < 0 then
			v_g := 0;
		elsif v_g > 255 then
			v_g := 255;
		end if;

		if v_b < 0 then
			v_b := 0;
		elsif v_b > 255 then
			v_b := 255;
		end if;

		if v_a < 0 then
			v_a := 0;
		elsif v_a > 255 then
			v_a := 255;
		end if;

		return v_r * power(2, 24) + v_g * power(2, 16) + v_b * power(2, 8) + v_a;
	end;

